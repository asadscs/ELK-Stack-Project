# Docker Install
$ docker run -it [container/name]
# Command to check available docker containers
$ docker ps -a
# Command to start a docker container
$ docker start [name]
# Command to attach a docker container
$ docker attach [name]
# Command to check installed docker images
$ docker images
# Ansible ping for webservers
$ ansible webservers -m ping
# Ansible ping for all
$ ansible all -m ping
# Command to check container logs
$ docker logs [name]
